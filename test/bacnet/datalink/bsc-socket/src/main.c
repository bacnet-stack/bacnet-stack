static void call_maintenance_timer(bool reset, int time_passed_ms)
{
    static int total_ms;
    if (reset) {
        total_ms = 0;
    }

    total_ms += time_passed_ms;

    if (total_ms >= 1000) {
        bsc_socket_maintenance_timer(1);
        total_ms = 0;
    }
}

static void wait_sec(int seconds)
{
    while (seconds >= 0) {
        bsc_wait(1);
        bsc_socket_maintenance_timer(1);
        seconds--;
    }
}

static bool wait_sock_ev(sock_ev_t *ev, BSC_SOCKET_EVENT wait_ev)
{
    call_maintenance_timer(1, 0);
    while (!bsc_event_timedwait(ev->ev, WAIT_EVENT_MS)) {
        call_maintenance_timer(0, WAIT_EVENT_MS);
    }
    bws_dispatch_lock();
    debug_printf(
        "wait_sock_ev ev = %p ev->ev = %p awaited_ev %d received_ev %d\n", ev,
        ev->ev, wait_ev, ev->ev_code);
    if (ev->ev_code == wait_ev) {
        ev->ev_code = -1;
        // reset event if it was signalled while we were blocked in call
        // bws_dispatch_lock(). (in that case ev->ev contains code of last
        // event.) that's tricky but that allows to avoid using mutexes which
        // are platform specific in test code
        bsc_event_timedwait(ev->ev, 1);
        bws_dispatch_unlock();
        return true;
    } else {
        debug_printf(
            "wait_sock_ev ev = %p ev->ev = %p awaited_ev %d received_ev %d\n",
            ev, ev->ev, wait_ev, ev->ev_code);
        ev->ev_code = -1;
        bws_dispatch_unlock();
        return false;
    }
}

static void wait_for_srv_specific_socket_state(
    BSC_SOCKET *s, BSC_SOCKET_STATE st)
{
    int i;
    call_maintenance_timer(1, 0);
    while (1) {
        bsc_wait_ms(WAIT_EVENT_MS);
        call_maintenance_timer(0, WAIT_EVENT_MS);
        bws_dispatch_lock();
        for (i = 0; i < MAX_SERVER_SOCKETS; i++) {
            if (&srv_socks[i] == s && srv_socks[i].state == st) {
                bws_dispatch_unlock();
                return;
            }
        }
        bws_dispatch_unlock();
    }
}

static void wait_specific_sock_ev(sock_ev_t *ev, BSC_SOCKET_EVENT wait_ev)
{
    call_maintenance_timer(1, 0);
    while (1) {
        while (!bsc_event_timedwait(ev->ev, WAIT_EVENT_MS)) {
            call_maintenance_timer(0, WAIT_EVENT_MS);
        }
        bws_dispatch_lock();
        debug_printf("wait_specific: wait_sock_ev ev = %p awaited_ev %d "
                     "received_ev %d\n",
            ev, wait_ev, ev->ev_code);
        if (ev->ev_code == wait_ev) {
            ev->ev_code = -1;
            // reset event if it was signalled while we were blocked in call
            // bws_dispatch_lock(). (in that case ev->ev contains code of last
            // event.) that's tricky but that allows to avoid using mutexes
            // which are platform specific in test code
            bsc_event_timedwait(ev->ev, 1);
            bws_dispatch_unlock();
            break;
        } else {
            debug_printf("wait_specific: event is ignored wait_sock_ev ev = %p "
                         "awaited_ev %d received_ev %d\n",
                ev, wait_ev, ev->ev_code);
            ev->ev_code = -1;
            bws_dispatch_unlock();
            continue;
        }
    }
}

static void signal_sock_ev(
    sock_ev_t *ev, BSC_SOCKET_EVENT s_ev, BACNET_ERROR_CODE err)
{
    debug_printf("signal_sock_ev %p ev->ev %p  s_ev %d\n", ev, ev->ev, s_ev);
    ev->ev_code = s_ev;
    ev->err = err;
    bsc_event_signal(ev->ev);
}

static bool wait_ctx_ev(ctx_ev_t *ev, BSC_CTX_EVENT wait_ev)
{
    call_maintenance_timer(1, 0);
    while (!bsc_event_timedwait(ev->ev, WAIT_EVENT_MS)) {
        call_maintenance_timer(0, WAIT_EVENT_MS);
    }

    bws_dispatch_lock();
    if (ev->ev_code == wait_ev) {
        bws_dispatch_unlock();
        return true;
    } else {
        bws_dispatch_unlock();
        return false;
    }
}

static void signal_ctx_ev(ctx_ev_t *ev, BSC_CTX_EVENT c_ev)
{
    ev->ev_code = c_ev;
    bsc_event_signal(ev->ev);
}

